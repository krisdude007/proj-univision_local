<p style="color:#636363">
    <strong>1. <u>Tipos de Información que Recolectamos</u></strong>
</p>
<p style="color:#636363">
    <strong><em>Información Personalmente Identificable</em></strong>
    (o "PII") es cualesquier información que puede identificar o está relacionada con un individuo en lo específico, tal y como su nombre, dirección de correo,
    número de teléfono (incluyendo número celular), dirección de correo electrónico, número de tarjeta de crédito, número de tarjeta de débito, número de
    cuenta bancaria, y otra información de cuentas financieras, fecha de nacimiento, género, ocupación, intereses personales, información demográfica,
    localización y de hogar, etc. Conforme al grado de que nos proporcione de manera voluntaria su PII, recolectaremos dicha información. Por ejemplo, podemos
    recolectar PII de usted (en la medida que la ley lo permita): (1) cuando se registre por uno de nuestros productos o servicios, como cuando usted se une a
    alguna de nuestras comunidades en Internet (incluyendo las áreas de conexión social en Univision.com y en nuestras páginas de fanáticos en sitios de medios
    de terceros); (2) cuando se apunte en una de nuestras listas de correos o de contactos; (3) cuando se registre para participar en un evento que sea
    patrocinado por nosotros (tal como concursos, sorteos, regalos, promociones, encuestas, conciertos, festivales, ferias de comunidad, etc.); (4) cuando
    realice alguna transacción con nosotros, tal y como ordenar uno de nuestros productos a través de nuestra tienda electrónica; (5) por utilizar o
    interactuar con un producto o servicio de la Red Interactiva de Univision; (6) al proporcionar o compartir información usando un producto o servicio de la
    Red Interactiva de Univision; (7) cuando usted envíe información a través de mensajes, chat, blog, correo postal o uno similar utilizando un producto o
    servicio de la Red Interactiva de Univision; (8) cuando usted realicé, comparta o vincule su perfil a través de un producto o servicio de la Red
    Interactiva de Univision, incluyendo la vinculación o la asociación de su perfil en un sitio de terceros o en una plataforma con su cuenta de Univision,
    y/o (9) cuando nos presente una solicitud o consulta a través de la Red Interactiva de Univision o cualquier otra (cada uno de los anteriores se referirá
    en lo sucesivo conjuntamente como una <strong>"Actividad de Identificación"</strong>).
</p>
<p style="color:#636363">
    Es opcional para usted que pueda participar en una Actividad de Identificación. Sin embargo, si usted elige participar en una Actividad de Identificación,
    es posible que le pidamos cierta información PII. Dependiendo de la actividad, parte de la información que le solicitamos que nos proporcione puede ser
    identificada como obligatoria y otra puede ser identificada como opcional. Si usted no proporciona la información obligatoria para una Actividad de
    Identificación particular que lo requiera, usted no será capaz de participar en esa actividad. Además, tenga en cuenta que si utiliza la Red Interactiva de
    Univision para enviar información a otra persona (por ejemplo, un amigo, un miembro de la familia, etc.), también vamos a recolectar información PII de esa
    otra persona en la medida divulgada por usted. También obtenemos el nombre de usuario y la contraseña para la cuenta de correo electrónico que desea
    importar sus contactos de, por ejemplo, y sólo usar esa información para ese propósito.
</p>
<p style="color:#636363">
    La <strong><em>Información Global y Anónima</em></strong> (en lo sucesivo <strong>"AAI"</strong>) es cualesquier información que no pueda identificar ni
    ser relacionada con algún individuo en específico. Por ejemplo, podemos recolectar información anónima de grupos (demográficos, hábitos de medios, hábitos
    de búsquedas por Internet, intereses y preferencias, etc.) relacionada con televidentes y radioescuchas de nuestros programas de televisión y radio,
    participantes de nuestras actividades y eventos patrocinados, usuarios y miembros de la Red Interactiva de Univision, etc. (en lo sucesivo referidos
    conjuntamente como <strong>"nuestra Audiencia"</strong>). También recolectamos información global de tráfico relacionada con la Red Interactiva de
    Univision, tal y como: visitas totales de la Red Interactiva de Univision (o una parte de la misma) en un tiempo determinado; impresiones totales por
    anuncio; cantidad promedio de tiempo que permanecen los visitantes en cada página de una parte o más de la Red Interactiva de Univision; porcentaje de
    visitantes de cierta área geográfica, etc. Además, podemos combinar "Información de Actividades de Usuarios" (como se describe a continuación) acerca de
    usted y otros usuarios de la Red Interactiva de Univision para generar información global que refleje actividades, hábitos, preferencias, intereses, etc.,
    de nuestros usuarios como grupo. Nosotros también recolectamos - información básica tal y como el código postal, ciudad y estado. Adicionalmente, podemos
    tomar PII y transformarlo en información no identificable como personal, por ejemplo agregando su información con información de otros individuos, o al
    remover elementos personales identificables (tal y como nombres) para "anonimizar" o "des-personalizar" su información.
</p>
<p style="color:#636363">
    <strong><em>Información de Actividades de Usuarios </em></strong>
    (en lo sucesivo <strong>"UAI"</strong>) se refiere a la información que recolectamos relacionada con las actividades de usuarios en la Red Interactiva de
    Univision, por ejemplo, nuestros servidores Web automáticamente recolectan y conectan "datos clickstream" para todos los usuarios de Univision.com, tal y
    como direcciones IP (que son referencias numéricas que son automáticamente asignadas a las computadoras y aparatos móviles de los usuarios cuando están
    navegando por Internet), solicitudes de páginas, páginas visitadas, contenido observado, "clicks" y búsquedas realizadas, etc. Asimismo utilizamos
    "cookies" y tecnologías similares para recolectar UAI en la Red Interactiva de Univision (ver <strong>Sección 5</strong> a continuación para más
    información). En la medida y conforme al grado que utilicemos cualesquier UAI de una manera que identifique a un usuario específicamente (por ejemplo, si
    ligamos o asociamos dicha información con el nombre o domicilio de un usuario), será considerado como PII. De otra forma, todo el UAI será tratado como
    AAI.
</p>
