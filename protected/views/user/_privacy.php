    está obligado a comunicarle por escrito a tal individuo cuando lo solicite, el tipo de información que fue revelada y a quien. El negocio puede cumplir con
    esta ley mediante: (1) CUALQUIER política de privacidad publicada de no compartir la información personal de un cliente para el uso de la comercialización
    directa de terceros a menos que el cliente previamente haya optado por tal divulgación, o a una política de privacidad publicada de no compartir la
    información personal de un cliente para el uso de la comercialización directa de terceros si el cliente ha optado hacia fuera de evitar que su información
    personal sea compartida para el uso de la comercialización directa; y (2) notificando al cliente de su derecho de salida y de proveer medios, libres de
    costo, para que el cliente ejerza ese derecho.
