Cualquier petición de divulgación requerida bajo esta ley citada debe ser enviada a    <a href="mailto:caprivacy@correo.univision.com">caprivacy@correo.univision.com</a>. Tome en cuenta que bajo esta ley, nosotros no estamos obligados a
